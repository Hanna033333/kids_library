def main():
    print("🚀 [DB 태그 마이그레이션] 데이터 로드 중...")
    
    # 1. 모든 도서 정보 페칭
    try:
        response = supabase.table("childbook_items").select("id, title, description, keywords, curation_note, curation_tag").execute()
        books = response.data if hasattr(response, 'data') else response
        print(f"✅ DB 도서 {len(books)}권 로드 완료.")
    except Exception as e:
        print(f"❌ 데이터 로드 실패: {e}")
        return

    # 2. 각 책에 대해 키워드 매칭 및 curation_tag 업데이트 리스트 생성
    updated_count = 0
    for idx, b in enumerate(books):
        book_id = b.get("id")
        title = b.get("title") or ""
        desc = b.get("description") or ""
        keyw = b.get("keywords") or ""
        note = b.get("curation_note") or ""
        existing_tags = b.get("curation_tag") or ""
        
        # 태그 목록 파싱
        tags_list = [t.strip() for t in existing_tags.split(",") if t.strip()]
        
        full_text = f"{title} {desc} {keyw} {note} {existing_tags}"
        
        matched_any = False
        for theme, keywords in THEMES_CONFIG.items():
            formatted = f"#{theme}"
            if formatted in tags_list:
                continue # 이미 태그가 들어가 있으면 패스
                
            # 키워드 매칭 여부
            for kw in keywords:
                if kw in full_text:
                    tags_list.append(formatted)
                    matched_any = True
                    break
        
        if matched_any:
            new_tags_str = ",".join(tags_list)
            # DB 업데이트 수행
            try:
                supabase.table("childbook_items").update({"curation_tag": new_tags_str}).eq("id", book_id).execute()
                updated_count += 1
                if updated_count % 50 == 0:
                    print(f"⏳ 진행 중... {updated_count}권 업데이트 완료")
            except Exception as e:
                print(f"⚠️ [{book_id}] 업데이트 중 에러 발생: {e}")
                
    print(f"🎉 마이그레이션 완료! 총 {updated_count}권의 도서 태그가 보완되었습니다.")

if __name__ == "__main__":
    main()
